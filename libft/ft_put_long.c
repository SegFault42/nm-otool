#include "./includes/libft.h"

void	ft_put_long(long n)
{
	if (n == -9223372036854775807 - 1)
	{
		ft_putstr("-9");
		n = 223372036854775808;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n > 9)
	{
		ft_put_long(n / 10);
		ft_put_long(n % 10);
	}
	else
		ft_putchar('0' + n);
}
